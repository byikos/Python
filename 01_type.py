#Tipos de datos en python 

#tipo str --> string 
print(type("soy un dato str"))
#tipo int --> entero
print(type(5))
#tipo float --> flotante
print(type(1.5))
#tipo bool --> boleano
print(type(True))
#tipo complex --> numero complejo
print(type(3 + 1j))

#***tipo NoneType --> tipo de dato nulo***
print(type(print("Mi cadena de texto")))

